      <p>
The first step in our process involves <a href="#"> providing us with your basic biographical information </a>. 
 Once we have had a chance to review it, we will reach out with a tailored set of additional questions that will help us get to know you better, and help us understand if Physis might be the right fit for you.   At that point, we may then invite you to meet with us for an initial one-on-one meet and greet where you can get to know the Physis team in person, and we can really get a sense of what you want in your next serious relationship. 

We like to be upfront about the fact that there is a nominal fee for this initial meeting.   This meeting can often take up to 90 minutes, and during that period you’ll have the undivided attention of our team, so the fee is strictly to cover our professional time.  We would love to be able to offer complimentary screenings but, as a boutique firm, our time is limited, and we find that charging a nominal early fee helps us screen out those who aren’t truly serious about this process.  Financially, it also allows us to carve out the time to focus solely on our conversation, and our prospective clients tend to appreciate that extra attention.

During this time we’ll ask you a lot of very specific questions that cover topics ranging from religions to ethnicities to occupations to eye colors, and we’ll work together to build an archetype of the sort of woman who might be a great fit for you at this point in your life.  Some of our questions will seem very superficial, some of them will seem very personal, and some of them will likely seem totally insane, but they all have a purpose.  At this point we’ll also talk about the possible Physis membership level that might make sense for you, and how best to proceed. 
And then we’ll actually ask you to take a few days to think about it.  All of it.  Does this feel right for you?  Is this the right time in your life to pursue it?  Are we the right team?  Does the membership we proposed make the most sense?  Are there maybe some things you forgot to mention?  Or would like to find?  Or don’t matter as much as you thought they might?  Are you ready to take the next step?
Once we all agree that it makes sense to work together, and decide how we’d like to do that, we’ll meet again.  We’ll refine your list of wants and wishes even further, and we’ll present you with our understanding of the criteria that are most important to you in this search.  Once we all clearly have your type and objectives crystallized, we move forward with our search so that you can move forward with the rest of your life!
